#include "libft.h"

void	ft_strclr(char *s)
{
	int i;

	if (!s)
		return ;
	i = 0;
	while (s[i] != '\0')
		i++;
	while (i > -1)
	{
		s[i] = '\0';
		i--;
	}
}
