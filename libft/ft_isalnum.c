#include "libft.h"

int		ft_isalnum(int c)
{
	return ((c > 'a' - 1 && c < 'z' + 1) || (c > 'A' - 1 && c < 'Z' + 1) ||
			(c > '0' - 1 && c < '9' + 1));
}
